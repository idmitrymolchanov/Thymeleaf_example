package project.model;

public class Vote {
    private String voteYes;
    private String voteNo;

    public Vote() {
    }

    public String getVoteYes() {
        return voteYes;
    }

    public void setVoteYes(String voteYes) {
        this.voteYes = voteYes;
    }

    public String getVoteNo() {
        return voteNo;
    }

    public void setVoteNo(String voteNo) {
        this.voteNo = voteNo;
    }
}
